class cal:
    def __init__(self,a,b,c):
            self.a = a
            self.b = b
            self.c = c
            print("a + b = ",self.a + self.b)
            print("a - b = ",self.a - self.b)
            print("a * b = ",self.a * self.b)
            print("a / b = ",self.a / self.b)
            print("a ** 2 = ",self.a ** 2)
            print("a ** 3 = ",self.a ** 3)
            print("a * b * c = ",self.a * self.b * self.c)   

